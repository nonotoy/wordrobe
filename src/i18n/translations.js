// Helper function to get translation with variable substitution
export function getTranslation(lang, key, variables = {}) {
  const langTranslations = translations[lang] || translations.ja;
  let text = langTranslations[key] || translations.ja[key] || key;

  // Replace variables like {query} with actual values
  Object.keys(variables).forEach(varName => {
    text = text.replace(`{${varName}}`, variables[varName]);
  });

  return text;
}
